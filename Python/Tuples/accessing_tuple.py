'''

- Values in a tuple are accessed using indices, similar to list.
- Indices start from 0 (first element) and can also be negative (-1 for the last element)

'''

# People Information as tuple format
salary = (41412412,15412125,1515151)

# Accessing tuple values using indices
second_element = salary[1]
last_element = salary [-1]

# Print the accessed values
print('Second Element : ', second_element)
print('Last Element : ', last_element)