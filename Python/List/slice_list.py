'''
- Slicing allows to extract a part of the list
- It is done using a colon (:) to specify start and step indices
'''

# Create a list with various data types
my_list = [10, 20, 30,40, 50,60,70]

# Slicing the list from index 1 to 4
slice_list = my_list[1:4]


# Print the sliced list
print("Sliced List: ", slice_list)
