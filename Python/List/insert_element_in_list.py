'''
- append() or insert() is used for add elements to a list.
- append() adds the element to end of the list
- insert() adds the elements at a specific index
'''

# Create a list with various data types
my_list = [10, 20, 30,40, 50]

# Modifying the first element
my_list.append(4)
my_list.insert(1,'Inserted')

# Print the modified list
print("Updating List: ", my_list)
