'''
- Retrive all keys, values, or key-value pairs using the keys(), and items() methods. 

'''

# Create a Dictionary named people_info with various key-value-pairs
people_info = {'name':'John', 'age':25, 'city':"New York"}

# Retrive  key,value and items
keys = people_info.keys()
values = people_info.values()
items = people_info.items()

# Print the  dictionary after removing
print("Keys : ", keys)
print("Values : ", values)
print("Items : ", items)